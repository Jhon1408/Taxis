    private void RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_RegistrarActionPerformed
        String user = NombreIn.getText();
        String pass = ContraseñaIn.getText();
        String confirm = ContraseñaInConfirm.getText();
        PreparedStatement Statement;
        try {
            if((user.length() <= 3 || user.length() > 20) || !user.matches("[A-Za-z0-9]+")) {
                JOptionPane.showMessageDialog(null, "Usuario invalido.\n");
                NombreIn.setText("");
                ContraseñaIn.setText("");
                ContraseñaInConfirm.setText("");
            } else {
                Statement = Request.getContext("LOGIN", "SEARCH_USER");
                Statement.setString(1, user);
                Statement.execute();
                ResultSet result = Statement.getResultSet();
                if (result.next() && result.getString("user").equals(user)) {
                    JOptionPane.showMessageDialog(null, "Nombre de usuario no disponible.\n");
                    NombreIn.setText("");
                    ContraseñaIn.setText("");
                    ContraseñaInConfirm.setText("");                   
                } else {
                    if((pass.length() <= 3 || pass.length() > 20) || !pass.matches("[A-Za-z0-9]+")) {
                        JOptionPane.showMessageDialog(null, "Contraseña invalida.\n");
                        ContraseñaIn.setText("");
                        ContraseñaInConfirm.setText("");
                    } else {
                        if(!pass.equals(confirm)) {
                            JOptionPane.showMessageDialog(null, "Las contraseñas no coinciden.\n");
                            ContraseñaIn.setText("");
                            ContraseñaInConfirm.setText("");
                        } else {
                            String salt = Arrays.toString(generateSalt());
                            pass = pass.concat(salt);
                            MessageDigest m = MessageDigest.getInstance("MD5");
                            m.reset();
                            m.update(pass.getBytes());
                            byte[] digest = m.digest();
                            BigInteger bigInt = new BigInteger(1,digest);
                            String hashtext = bigInt.toString(16);
                            Statement = Request.getContext("LOGIN", "INSERT");
                            Statement.setString(1, user);
                            Statement.setString(2, hashtext);
                            Statement.setString(3, salt);
                            Statement.executeUpdate();
                            JOptionPane.showMessageDialog(null, "Registro exitoso.\n");
                            NombreIn.setText("");
                            ContraseñaIn.setText("");
                            ContraseñaInConfirm.setText(""); 
                            login.setVisible(true);
                            this.setVisible(false);
                        }
                    }
                }
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, "Se ah detectado un problema.\n"+e);
        } catch (NoSuchAlgorithmException ex) {
            Logger.getLogger(Register.class.getName()).log(Level.SEVERE, null, ex);
        }           
    }//GEN-LAST:event_RegistrarActionPerformed

